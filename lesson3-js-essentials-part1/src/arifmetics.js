// Числові змінні
const a = 10;
const b = 3;

//  Арифметика між числами
console.log('Сума чисел:', a + b);         // 13
console.log('Різниця чисел:', a - b);      // 7
console.log('Добуток чисел:', a * b);      // 30
console.log('Ділення чисел:', a / b);      // 3.333...

// Рядкові змінні
const str1 = '5';
const str2 = '2';

//  Арифметика між рядками (якщо перетворити)
console.log('Сума рядків як чисел:', Number(str1) + Number(str2)); // 7

//  Змішана арифметика: число + рядок
console.log('Число + рядок:', a + str1);   // "105" (конкатенація)

//  Булеві змінні
const bool1 = true;  // true → 1
const bool2 = false; // false → 0

//  Арифметика між булевими
console.log('Булеве додавання:', bool1 + bool2); // 1

//  Змішана арифметика: число + булеве
console.log('Число + булеве:', a + bool1); // 11

//  Змішана арифметика: рядок + булеве
console.log('Рядок + булеве:', str1 + bool2); // "5false"
